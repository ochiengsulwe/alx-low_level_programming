#include "main.h"
#include <stdio.h>
#include <string.h>

/**
 * _strncat - concatenates two strings
 * @dest: destination string
 * @src: source string
 * @n: an integer to work on
 *
 * return: p
 */

char *_strncat(char *dest, char *src, int n);
{
	char *p;

	strncat(dest, src, n);
	p = strncat;

	return (p);
}

